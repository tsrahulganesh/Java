package Arrays;

public class Array4 {
    public static void main(String[] args) {
        String[] data = new String[4];
        data[0] = "abc";
        data[1] = "b";
        data[2] = "c";
        data[3] = "d";
        for (char i = 0; i < data.length; i++) {
            System.out.println(data[i]);

        }
    }
}
